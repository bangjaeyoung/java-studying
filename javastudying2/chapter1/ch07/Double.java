package chapter1.ch07;
public class Double {

    public static void main(String[] args) {

        double dNum  = 3.14;
        float fNum = 3.14F;
        //기본적으로 실수 리터럴은 double로 저장되어있기 때문에 더 큰 자료형으로 선언할 때는 숫자 뒤에 F를 써줌

        System.out.println(dNum);
        System.out.println(fNum);

    }
    
}
