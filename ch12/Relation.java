package ch12;

public class Relation {

	public static void main(String[] args) {
		
		int num1 = 5;
		int num2 = 3;
		
		boolean val = (num1 > num2);
		System.out.println(val);
		
		System.out.println(num1 < num2);
		System.out.println(num1 >= num2);
		System.out.println(num1 <= num2);
		System.out.println(num1 == num2);
		System.out.println(num1 != num2);
		
	}

}
